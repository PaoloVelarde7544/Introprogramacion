num1 = int(input("ingrese el primer numero: "))
num2 = int(input("ingrese el segundo numero: "))
son_iguales = num1 == num2
print (f"los numeros son iguales: {son_iguales}")

son_distintos = num1 != num2
print (f"los numeros son diferentes: {son_distintos}")

num1_mayor_que_num2 = num1>num2
print (f"num1 es mayor que num2: {num1_mayor_que_num2}")

num1_menor_o_igual_que_num2 = num1<=num2
print (f"num1 es menor o igual que num2: {num1_menor_o_igual_que_num2}")